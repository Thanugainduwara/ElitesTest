def add(x, y):
    """Function to add two numbers"""
    return x + y

def sub(x, y):
    """Function to subtract two numbers"""
    return x - y

def mul(x, y):
    """Function to multiply two numbers"""
    return x * y

def div(x, y):
    """Function to divide two numbers"""
    if y == 0:
        return "Error! Division by zero."
    else:
        return x / y

